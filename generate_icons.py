def create_simple_png(size: int, filepath: str):
    """Create a simple colored PNG icon."""
    try:
        from PIL import Image, ImageDraw
        
        # Create image with dark background
        img = Image.new('RGBA', (size, size), (26, 26, 46, 255))
        draw = ImageDraw.Draw(img)
        
        # Draw lock body (red/pink rectangle)
        body_margin = int(size * 0.25)
        body_top = int(size * 0.42)
        body_height = int(size * 0.4)
        radius = int(size * 0.06)
        
        draw.rounded_rectangle(
            [body_margin, body_top, size - body_margin, body_top + body_height],
            radius=radius,
            fill=(233, 69, 96, 255)
        )
        
        # Draw shackle (arc above lock)
        shackle_margin = int(size * 0.35)
        shackle_top = int(size * 0.15)
        shackle_width = int(size * 0.08)
        
        draw.arc(
            [shackle_margin, shackle_top, size - shackle_margin, body_top + int(size*0.1)],
            start=180,
            end=0,
            fill=(233, 69, 96, 255),
            width=shackle_width
        )
        
        # Draw keyhole
        keyhole_x = size // 2
        keyhole_y = body_top + int(body_height * 0.35)
        keyhole_r = int(size * 0.06)
        
        draw.ellipse(
            [keyhole_x - keyhole_r, keyhole_y - keyhole_r, 
             keyhole_x + keyhole_r, keyhole_y + keyhole_r],
            fill=(26, 26, 46, 255)
        )
        
        # Save
        img.save(filepath, 'PNG')
        print(f"Created {filepath}")
        return True
        
    except ImportError:
        print("Pillow not installed, using fallback icons")
        return False


def main():
    icons_dir = os.path.dirname(os.path.abspath(__file__))
    
    for size in [16, 48, 128]:
        filepath = os.path.join(icons_dir, f'icon{size}.png')
        if not create_simple_png(size, filepath):
            # Fallback: create minimal valid PNG
            print(f"Creating minimal icon{size}.png")


if __name__ == '__main__':
    main()
